#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 1;
	if (lst != 0)
	{
		while (lst->next != 0)
		{
			i++;
			lst = lst->next;
		}
	}
	else
		return (0);
	return (i);
}
