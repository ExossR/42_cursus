int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb == 1)
		return (1);
	while (i < 46341 && i < (nb / 2))
	{
		if (nb == (i * i))
			return (i);
		i++;
	}
	return (0);
}

