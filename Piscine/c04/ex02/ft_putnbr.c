#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	x;

	x = 0;
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		x = nb % 10 + 48;
		write(1, &x, 1);
	}
	else if (nb < 10 && nb >= 0)
	{
		nb += 48;
		write(1, &nb, 1);
	}
}

